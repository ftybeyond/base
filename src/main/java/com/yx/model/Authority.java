package com.yx.model;

public class Authority {
    private Integer id;

    private String name;

    private String moudle;

    private String funIcon;

    private String url;

    private String mouIcon;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getMoudle() {
        return moudle;
    }

    public void setMoudle(String moudle) {
        this.moudle = moudle;
    }

    public String getFunIcon() {
        return funIcon;
    }

    public void setFunIcon(String funIcon) {
        this.funIcon = funIcon;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getMouIcon() {
        return mouIcon;
    }

    public void setMouIcon(String mouIcon) {
        this.mouIcon = mouIcon;
    }
}